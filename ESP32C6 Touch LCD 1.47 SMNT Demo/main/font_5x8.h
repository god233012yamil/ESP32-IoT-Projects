/**
 * @brief Get font glyph for a character
 * @param c ASCII character (32-126)
 * @return Pointer to 5-byte glyph data, or NULL if out of range
 */
static inline const uint8_t* font_5x8_get_glyph(char c) {
    if (c < 32 || c > 126) {
        return font_5x8[0];  // Return space for invalid characters
    }
    return font_5x8[c - 32];
}

/**
 * @brief Check if character is printable (has a glyph)
 * @param c ASCII character
 * @return true if character is printable, false otherwise
 */
static inline int font_5x8_is_printable(char c) {
    return (c >= 32 && c <= 126);
}

